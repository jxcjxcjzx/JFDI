var cCommand = require('../../config/command'),
    specialCommands = cCommand.specialCommands,
    knownCommands = cCommand.knownCommands;

/**
 *
 */
exports.isSpecialCommand = function(initialCommand) {
    return specialCommands.indexOf(initialCommand) > -1;
};

/**
 *
 */
exports.isKnownCommand = function(command) {
    return knownCommands.indexOf(command) > -1;
};
